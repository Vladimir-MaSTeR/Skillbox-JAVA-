public class CucleWhile {

    public static void main(String[] args) {

        int i = 200000;
        int j = 220000;
        //-------------------------------------------
        while (i <= 210000) {
            System.out.println(i);
            i++;
        }

        while (j <= 235000) {
            System.out.println(j);
            j++;
        }
    }
}
